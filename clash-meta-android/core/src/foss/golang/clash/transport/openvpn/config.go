package openvpn

import (
	"bytes"
	"encoding/hex"
	"encoding/pem"
	"errors"
	"fmt"
	"net"
	"strconv"
	"strings"
	"time"
)

const (
	ProtoUDP = "udp"
	ProtoTCP = "tcp"

	CipherAES128GCM        = "AES-128-GCM"
	CipherAES192GCM        = "AES-192-GCM"
	CipherAES256GCM        = "AES-256-GCM"
	CipherAESCBC           = "AES-CBC"
	CipherAES128CBC        = "AES-128-CBC"
	CipherAES192CBC        = "AES-192-CBC"
	CipherAES256CBC        = "AES-256-CBC"
	CipherChaCha20Poly1305 = "CHACHA20-POLY1305"
	AuthMD5                = "MD5"
	AuthSHA1               = "SHA1"
	AuthSHA256             = "SHA256"
	AuthSHA384             = "SHA384"
	AuthSHA512             = "SHA512"

	CompLzoYes = "yes"
)

// ClientConfig is the normalized subset generated by hwdsl2/openvpn-install.
type ClientConfig struct {
	RemoteHost string
	RemotePort uint16
	Proto      string
	Dev        string
	Cipher     string
	Auth       string
	CompLZO    string

	CA       []byte
	Cert     []byte
	Key      []byte
	TLSAuth  []byte
	TLSCrypt []byte

	KeyDirection string

	Username string
	Password string

	PeerInfo map[string]string

	PingInterval time.Duration
	PingRestart  time.Duration

	TLSCryptKey []byte
	TLSAuthKey  []byte

	// TLSCryptV2Key is the first 256 bytes of tls-crypt-v2 client key material.
	TLSCryptV2Key []byte
	// TLSCryptV2WrappedKey is appended to the initial V3 hard-reset packet.
	TLSCryptV2WrappedKey []byte
	// TLSCryptV2 is the raw PEM-encoded OpenVPN tls-crypt-v2 client key.
	TLSCryptV2 []byte

	// DataCiphers is the list of data channel ciphers the client offers to
	// the server for negotiation (OpenVPN --data-ciphers). If non-empty, the
	// negotiated cipher is the first entry in the server's pushed cipher list
	// that also appears here. If empty, the single Cipher field is used.
	DataCiphers []string

	// FallbackCipher is used when the server does not support cipher
	// negotiation (OpenVPN --data-ciphers-fallback).
	FallbackCipher string
}

// DataCipherKeyLength returns the key size for the negotiated data cipher.
func (c ClientConfig) DataCipherKeyLength() int {
	return CipherKeyLength(c.Cipher)
}

// CipherKeyLength returns the key size for a given cipher name.
func CipherKeyLength(cipher string) int {
	switch cipher {
	case CipherAES256GCM, CipherAES256CBC, CipherChaCha20Poly1305:
		return 32
	case CipherAES192GCM, CipherAES192CBC:
		return 24
	default:
		return 16
	}
}

// NegotiateCipher selects the data channel cipher based on the server-pushed
// cipher list and the locally configured DataCiphers list.
//
// If the server pushed a cipher list (via "data-ciphers" or "cipher" push
// options), the result is the first cipher in the server's list that also
// appears in the local DataCiphers list. If no intersection exists and a
// FallbackCipher is configured, it is used. If no fallback is configured but
// the server pushed a single cipher that matches the local Cipher, that is
// used.
//
// If the server did not push any cipher options, the local Cipher is used
// as-is (legacy behavior for servers without cipher negotiation).
func (c ClientConfig) NegotiateCipher(pushedCiphers []string, pushedCipher string) (string, error) {
	if len(pushedCiphers) == 0 && pushedCipher == "" {
		return c.Cipher, nil
	}

	if len(c.DataCiphers) > 0 {
		serverList := pushedCiphers
		if len(serverList) == 0 && pushedCipher != "" {
			serverList = []string{pushedCipher}
		}
		for _, serverCipher := range serverList {
			normalized := normalizeCipher(serverCipher)
			for _, localCipher := range c.DataCiphers {
				if normalizeCipher(localCipher) == normalized {
					return normalized, nil
				}
			}
		}
		if c.FallbackCipher != "" {
			return normalizeCipher(c.FallbackCipher), nil
		}
		return "", fmt.Errorf("no common data cipher between client %v and server %v", c.DataCiphers, serverList)
	}

	if pushedCipher != "" {
		normalized := normalizeCipher(pushedCipher)
		if normalized == c.Cipher {
			return normalized, nil
		}
		if c.FallbackCipher != "" {
			return normalizeCipher(c.FallbackCipher), nil
		}
		if isSupportedCipher(normalized) {
			return normalized, nil
		}
		return "", fmt.Errorf("server pushed unsupported cipher %q", pushedCipher)
	}

	if len(pushedCiphers) > 0 {
		for _, sc := range pushedCiphers {
			normalized := normalizeCipher(sc)
			if isSupportedCipher(normalized) {
				if c.FallbackCipher != "" {
					return normalizeCipher(c.FallbackCipher), nil
				}
				return normalized, nil
			}
		}
	}

	return c.Cipher, nil
}

// isSupportedCipher returns true if the cipher name is recognized.
func isSupportedCipher(cipher string) bool {
	switch cipher {
	case CipherAES128GCM, CipherAES192GCM, CipherAES256GCM,
		CipherAES128CBC, CipherAES192CBC, CipherAES256CBC,
		CipherChaCha20Poly1305:
		return true
	default:
		return false
	}
}

func (c ClientConfig) RemoteAddress() string {
	return net.JoinHostPort(c.RemoteHost, strconv.Itoa(int(c.RemotePort)))
}

func normalizeProto(proto string) string {
	switch strings.ToLower(strings.TrimSpace(proto)) {
	case "":
		return ProtoUDP
	case "udp", "udp4":
		return ProtoUDP
	case "tcp", "tcp-client", "tcp4", "tcp4-client":
		return ProtoTCP
	default:
		return strings.ToLower(strings.TrimSpace(proto))
	}
}

func normalizeCipher(cipher string) string {
	switch strings.ToUpper(strings.TrimSpace(cipher)) {
	case "":
		return CipherAES128GCM
	case CipherAESCBC:
		return CipherAES128CBC
	default:
		return strings.ToUpper(strings.TrimSpace(cipher))
	}
}

func normalizeAuth(auth string) string {
	switch strings.ToUpper(strings.TrimSpace(auth)) {
	case "":
		return AuthSHA256
	case "SHA-1":
		return AuthSHA1
	default:
		return strings.ToUpper(strings.TrimSpace(auth))
	}
}

func normalizeCompLZO(compLZO string) string {
	switch strings.ToLower(strings.TrimSpace(compLZO)) {
	case "yes", "adaptive":
		return CompLzoYes
	default:
		return strings.ToLower(strings.TrimSpace(compLZO))
	}
}

func (c *ClientConfig) Prepare() error {
	if c == nil {
		return errors.New("nil openvpn client config")
	}
	c.RemoteHost = strings.TrimSpace(c.RemoteHost)
	c.Proto = normalizeProto(c.Proto)
	if c.Dev == "" {
		c.Dev = "tun"
	}
	c.Dev = strings.ToLower(strings.TrimSpace(c.Dev))
	c.Cipher = normalizeCipher(c.Cipher)
	c.Auth = normalizeAuth(c.Auth)
	c.CompLZO = normalizeCompLZO(c.CompLZO)

	if err := c.ValidateInstallScriptSubset(); err != nil {
		return err
	}
	if len(bytes.TrimSpace(c.TLSAuth)) > 0 {
		key, err := DecodeStaticKey(c.TLSAuth)
		if err != nil {
			return fmt.Errorf("parse tls-auth key: %w", err)
		}
		c.TLSAuthKey = key
	}
	if len(bytes.TrimSpace(c.TLSCrypt)) > 0 {
		key, err := DecodeStaticKey(c.TLSCrypt)
		if err != nil {
			return fmt.Errorf("parse tls-crypt key: %w", err)
		}
		c.TLSCryptKey = key
	}
	if len(bytes.TrimSpace(c.TLSCryptV2)) > 0 {
		keyMaterial, wrappedKey, err := DecodeTLSCryptV2ClientKey(c.TLSCryptV2)
		if err != nil {
			return fmt.Errorf("parse tls-crypt-v2 client key: %w", err)
		}
		c.TLSCryptV2Key = keyMaterial
		c.TLSCryptV2WrappedKey = wrappedKey
	}
	return nil
}

func (c *ClientConfig) ValidateInstallScriptSubset() error {
	if c == nil {
		return errors.New("nil openvpn client config")
	}
	if c.RemoteHost == "" || c.RemotePort == 0 {
		return errors.New("openvpn config requires remote host and port")
	}
	if c.Dev != "tun" {
		return fmt.Errorf("unsupported openvpn dev %q: only dev tun is supported", c.Dev)
	}
	if c.Proto != ProtoUDP && c.Proto != ProtoTCP {
		return fmt.Errorf("unsupported openvpn proto %q: only udp and tcp are supported", c.Proto)
	}
	if c.Cipher != CipherAES128GCM && c.Cipher != CipherAES192GCM && c.Cipher != CipherAES256GCM && c.Cipher != CipherAES128CBC && c.Cipher != CipherAES192CBC && c.Cipher != CipherAES256CBC && c.Cipher != CipherChaCha20Poly1305 {
		return fmt.Errorf("unsupported openvpn cipher %q: only %s, %s, %s, %s, %s, %s and %s are supported", c.Cipher, CipherAES128GCM, CipherAES192GCM, CipherAES256GCM, CipherAES128CBC, CipherAES192CBC, CipherAES256CBC, CipherChaCha20Poly1305)
	}
	if c.Auth != AuthMD5 && c.Auth != AuthSHA1 && c.Auth != AuthSHA256 && c.Auth != AuthSHA384 && c.Auth != AuthSHA512 {
		return fmt.Errorf("unsupported openvpn auth %q: only %s, %s, %s, %s and %s are supported", c.Auth, AuthMD5, AuthSHA1, AuthSHA256, AuthSHA384, AuthSHA512)
	}
	if c.KeyDirection != "1" && c.KeyDirection != "0" && c.KeyDirection != "" {
		return fmt.Errorf("unsupported openvpn key-direction %q: only '1' and '0' are supported", c.KeyDirection)
	}
	if len(bytes.TrimSpace(c.TLSAuth)) > 0 && len(bytes.TrimSpace(c.TLSCrypt)) > 0 {
		return errors.New("openvpn tls-auth and tls-crypt are mutually exclusive")
	}
	if len(bytes.TrimSpace(c.TLSCryptV2)) > 0 {
		if len(bytes.TrimSpace(c.TLSAuth)) > 0 || len(bytes.TrimSpace(c.TLSCrypt)) > 0 {
			return errors.New("openvpn tls-crypt-v2 is mutually exclusive with tls-auth and tls-crypt")
		}
	}
	for name, value := range map[string][]byte{
		"ca": c.CA,
	} {
		if len(bytes.TrimSpace(value)) == 0 {
			return fmt.Errorf("openvpn config requires inline <%s> block", name)
		}
	}
	if block, _ := pem.Decode(c.CA); block == nil {
		return errors.New("inline <ca> block is not PEM")
	}
	hasCert := len(bytes.TrimSpace(c.Cert)) > 0 || len(bytes.TrimSpace(c.Key)) > 0
	if hasCert {
		if len(bytes.TrimSpace(c.Cert)) == 0 || len(bytes.TrimSpace(c.Key)) == 0 {
			return errors.New("openvpn cert and key must both be set when using client certificate auth")
		}
		if block, _ := pem.Decode(c.Cert); block == nil {
			return errors.New("inline <cert> block is not PEM")
		}
		if block, _ := pem.Decode(c.Key); block == nil {
			return errors.New("inline <key> block is not PEM")
		}
	} else if strings.TrimSpace(c.Username) == "" {
		return errors.New("openvpn requires either cert+key or username (auth-user-pass)")
	}
	if c.PingInterval < 0 {
		return errors.New("openvpn ping interval must be positive")
	}
	if c.PingRestart < 0 {
		return errors.New("openvpn ping restart must be positive")
	}
	return nil
}

func DecodeStaticKey(block []byte) ([]byte, error) {
	var hexLines []string
	for _, raw := range strings.Split(string(block), "\n") {
		line := strings.TrimSpace(raw)
		if line == "" || strings.HasPrefix(line, "#") {
			continue
		}
		if strings.HasPrefix(line, "-----BEGIN OpenVPN Static key") || strings.HasPrefix(line, "-----END OpenVPN Static key") {
			continue
		}
		hexLines = append(hexLines, line)
	}
	encoded := strings.Join(hexLines, "")
	key, err := hex.DecodeString(encoded)
	if err != nil {
		return nil, err
	}
	if len(key) != 256 {
		return nil, fmt.Errorf("invalid static key length %d, expected 256 bytes", len(key))
	}
	return key, nil
}
