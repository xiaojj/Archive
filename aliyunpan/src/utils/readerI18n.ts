import { ref, computed } from 'vue'

export type ReaderLocale = 'zh' | 'en'

const currentLocale = ref<ReaderLocale>((localStorage.getItem('reader.locale') as ReaderLocale) || 'zh')

export function useReaderI18n() {
  const locale = computed(() => currentLocale.value)

  function setLocale(loc: ReaderLocale) {
    currentLocale.value = loc
    localStorage.setItem('reader.locale', loc)
  }

  function t(key: string): string {
    const map = messages[currentLocale.value] || messages.zh
    return map[key] || messages.zh[key] || key
  }

  return { locale, setLocale, t }
}

